package model;

public class EmployeeInfo {
    public String usrId;
    public String name;
    public String birthday;
    public String wage;
    public String email;

    public String getUsrId() {
        return usrId;
    }

    public String getName() {
        return name;
    }

    public String getBirthday() {
        return birthday;
    }

    public String getWage() {
        return wage;
    }

    public String getEmail() {
        return email;
    }
}
